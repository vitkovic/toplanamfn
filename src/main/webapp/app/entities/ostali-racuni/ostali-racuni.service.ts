import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IOstaliRacuni } from '@/shared/model/ostali-racuni.model';

const baseApiUrl = 'api/ostali-racunis';

export default class OstaliRacuniService {
  public find(id: number): Promise<IOstaliRacuni> {
    return new Promise<IOstaliRacuni>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IOstaliRacuni): Promise<IOstaliRacuni> {
    return new Promise<IOstaliRacuni>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IOstaliRacuni): Promise<IOstaliRacuni> {
    return new Promise<IOstaliRacuni>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
