import axios from 'axios';

import { ISifraDokumenta } from '@/shared/model/sifra-dokumenta.model';

const baseApiUrl = 'api/sifra-dokumentas';

export default class SifraDokumentaService {
  public find(id: number): Promise<ISifraDokumenta> {
    return new Promise<ISifraDokumenta>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ISifraDokumenta): Promise<ISifraDokumenta> {
    return new Promise<ISifraDokumenta>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ISifraDokumenta): Promise<ISifraDokumenta> {
    return new Promise<ISifraDokumenta>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
